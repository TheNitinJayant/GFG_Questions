package GFG_Questions.Tree;

public class TreeNode {
    public int key;
    public TreeNode left;
    public TreeNode right;

    TreeNode(){
        this.key = 0;
        this.left = null;
        this.right = null;
    }

    TreeNode(int data){
        this.key = data;
        this.left = null;
        this.right = null;
    }
}
